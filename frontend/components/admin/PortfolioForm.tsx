"use client";

import { useState, useEffect } from 'react';

// Re-using the interface from the Manager component, but it could be moved to a shared types file
interface PortfolioItem {
    id?: string;
    title: string;
    description: string;
    content?: string;
    image_url?: string;
    github_url?: string;
    demo_url?: string;
    tags: string[];
}

interface PortfolioFormProps {
  itemToEdit?: PortfolioItem | null;
  onSave: (item: PortfolioItem) => void;
  onCancel: () => void;
}

export default function PortfolioForm({ itemToEdit, onSave, onCancel }: PortfolioFormProps) {
  const [item, setItem] = useState<PortfolioItem>({
    title: '',
    description: '',
    content: '',
    image_url: '',
    github_url: '',
    demo_url: '',
    tags: [],
  });
  const [tagsInput, setTagsInput] = useState('');

  useEffect(() => {
    if (itemToEdit) {
      setItem(itemToEdit);
      setTagsInput(itemToEdit.tags.join(', '));
    } else {
      // Reset form for creation
      setItem({
        title: '',
        description: '',
        content: '',
        image_url: '',
        github_url: '',
        demo_url: '',
        tags: [],
      });
      setTagsInput('');
    }
  }, [itemToEdit]);

  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) => {
    const { name, value } = e.target;
    setItem(prev => ({ ...prev, [name]: value }));
  };

  const handleTagsChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setTagsInput(e.target.value);
    setItem(prev => ({ ...prev, tags: e.target.value.split(',').map(tag => tag.trim()).filter(tag => tag) }));
  }

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    const dataToSend = { ...item };
    // Explicitly remove id, as it's handled by URL param for updates and generated by backend for creates
    if (dataToSend.id) {
        delete dataToSend.id;
    }
    onSave(dataToSend);
  };

  return (
    <form onSubmit={handleSubmit} className="space-y-4 text-white">
      <div>
        <label htmlFor="title" className="block text-sm font-medium text-gray-400">Title</label>
        <input
          type="text"
          name="title"
          id="title"
          value={item.title}
          onChange={handleChange}
          required
          className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
        />
      </div>
       <div>
        <label htmlFor="description" className="block text-sm font-medium text-gray-400">Description</label>
        <textarea
          name="description"
          id="description"
          value={item.description}
          onChange={handleChange}
          required
          rows={3}
          className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
        />
      </div>
       <div>
        <label htmlFor="content" className="block text-sm font-medium text-gray-400">Content (HTML)</label>
        <textarea
          name="content"
          id="content"
          value={item.content || ''}
          onChange={handleChange}
          rows={6}
          className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
        />
      </div>
      <div>
        <label htmlFor="image_url" className="block text-sm font-medium text-gray-400">Image URL</label>
        <input
          type="url"
          name="image_url"
          id="image_url"
          value={item.image_url || ''}
          onChange={handleChange}
          className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
        />
      </div>
      <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
        <div>
          <label htmlFor="github_url" className="block text-sm font-medium text-gray-400">GitHub URL</label>
          <input
            type="url"
            name="github_url"
            id="github_url"
            value={item.github_url || ''}
            onChange={handleChange}
            className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
          />
        </div>
        <div>
          <label htmlFor="demo_url" className="block text-sm font-medium text-gray-400">Demo URL</label>
          <input
            type="url"
            name="demo_url"
            id="demo_url"
            value={item.demo_url || ''}
            onChange={handleChange}
            className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
          />
        </div>
      </div>
       <div>
        <label htmlFor="tags" className="block text-sm font-medium text-gray-400">Tags (comma-separated)</label>
        <input
          type="text"
          name="tags"
          id="tags"
          value={tagsInput}
          onChange={handleTagsChange}
          className="mt-1 block w-full px-3 py-2 bg-gray-700 border border-gray-600 rounded-md shadow-sm focus:outline-none focus:ring-indigo-500 focus:border-indigo-500"
        />
      </div>
      <div className="flex justify-end space-x-4 pt-4">
        <button
          type="button"
          onClick={onCancel}
          className="px-4 py-2 text-sm font-medium text-gray-300 bg-gray-600 rounded-md hover:bg-gray-500"
        >
          Cancel
        </button>
        <button
          type="submit"
          className="px-4 py-2 text-sm font-medium text-white bg-indigo-600 rounded-md hover:bg-indigo-700"
        >
          Save
        </button>
      </div>
    </form>
  );
}
